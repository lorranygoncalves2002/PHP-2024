// Função: Bloco de código Reutilizável

// A palavra reservada function define uma função
// É necessário definir um nome da função dentro de () vão ficar os parâmetros
// Ex de parâmetro: idade, nome, pedo
// Ex de argumento: 44, raquel, 70


function somarDoisValores(primeiroValor, segundoValor) {
    // Retorna a soma dos dois valores
    return primeiroValor + segundoValor;
}

// Trecho do código que executa a função, passando dois argumentos(77,70)
// Afunção faz aquilo que tem responsabilidade de fazer, quando um sistema tem muitas
// linhas ajuda muito(EX: Mais de 1M de linhas), é como se fosse um garçom 
// você pede um ítem e ele se vira para fazer aquilo, pouco importa como ele vai fazer
// o que você quer é apenas o ítem a ser retornado para você.

somarDoisValores(44,70);

